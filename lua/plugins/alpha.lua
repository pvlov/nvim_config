-- The lazy.nvim plugin specification
return {
    'goolord/alpha-nvim',
    lazy = false,    
	dependencies = { 'nvim-telescope/telescope.nvim' },
    config = function()
        local alpha = require("alpha")
        local dashboard = require("alpha.themes.dashboard")

        -- Define the new buttons
        local buttons = {
            type = "group",
            val = {
                dashboard.button("f", "  Search in Files", ":Telescope live_grep<CR>"),
                dashboard.button("r", "  Recent Files", ":Telescope oldfiles<CR>"),
                dashboard.button("n", "  New File", ":enew<CR>"),
                dashboard.button("q", "  Quit", ":qa<CR>"),
            },
            opts = {
                spacing = 1,
                position = "center",
            },
        }

        -- Setup alpha with your custom layout
        alpha.setup {
            opts = {
                noautocmd = true
            },
            layout = {
                {
                    type = "text",
                    val = function()
                        local pad = {}
                        -- Adjusted padding to account for the new buttons section
                        for _ = 1, ((vim.o.lines - 45) / 2) do
                            table.insert(pad, "")
                        end
                        return pad
                    end
                },
                header,
                buttons,
            }
        }
    end
}
